"""
有如下值集合[11,22,33,44,55,66,77,88,99,90]，
将所有大于66的值保存至字典的第一个key中，
将小于66的值保存至第二个key的值中。
即：{'k1':大于66的所有值,'k2':小于66的所有值}
"""


def fun():
    a = [11, 22, 33, 44, 55, 66, 77, 88, 99, 90]
    b = [i for i in a if i > 66]
    c = [i for i in a if i < 66]
    dict1 = {}
    dict1['k1'] = b
    dict1['k2'] = c
    print(dict1)


def fun1():
    a = [11, 22, 33, 44, 55, 66, 77, 88, 99, 90]
    a.sort()
    i = a.index(66)
    dict1 = {'k1': a[i + 1:], 'k2': a[:i]}
    print(dict1)

fun()
fun1()
